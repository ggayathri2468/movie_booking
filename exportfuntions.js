  let rightbutton = create("div");
  rightbutton.id = `${array[0].id}back_btn`;
  rightbutton.className = "back_next_btn";
  rightbutton.innerHTML = '<i class="fa-solid fa-chevron-right"></i>';
  recommond_image_container.appendChild(rightbutton);
  //
  let span_div = create("div");
  span_div.className = "scrolling_effect";
  span_div.id = `${array[0].id}_div`;
  recommond_image_container.appendChild(span_div);
  //
  array.forEach((item, i) => {
    let span = create("span");
    span.id = `${item.id}${i}`;
    // img
    let img_div = create("div");
    img_div.id = "";
    let img = create("img");
    img.className = img_size;
    img.id = "";
    img.src = `${item.src}`;
    img_div.appendChild(img);
    span.appendChild(img_div);
    // content
    let content_div = create("div");
    content_div.id = "";
    content_div.className = "contant_div_size";
    content_div.style.paddingTop = "10px";
    // title
    let title = create("div");
    title.className = "title_style";
    title.id = "";
    title.innerText = `${item.name}`;
    heading_text == "Premieres"
      ? (title.style.color = "White")
      : (title.style.color = "Black");
    content_div.appendChild(title);
    // discription
    let discription = create("div");
    discription.className = "discription_style";
    discription.id = "";
    discription.innerText = `${item.type}`;
    content_div.appendChild(discription);
    span.appendChild(content_div);
    span_div.appendChild(span);
  });
  // left button
  let leftbutton = create("div");
  leftbutton.className = "back_next_btn next_btn_postion";
  leftbutton.id = `${array[0].id}nextbtn`;
  leftbutton.innerHTML = '<i class="fa-solid fa-chevron-left"></i>';
