  recommond_container.appendChild(recommond_header);
  //  recommond image container
  let recommond_image_container = create("div");
  recommond_image_container.id = `${array[0].id}`;
  recommond_image_container.className = "d-flex container align-items-center";
  recommond_container.appendChild(recommond_image_container);
